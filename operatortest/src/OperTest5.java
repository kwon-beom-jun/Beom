public class OperTest5{
	public static void main(String[] args)	{
		int x = 0;
		System.out.println("1. x =" + x);
		x = 10;
		System.out.println("2. x =" + x);
		x += 5; // x = x + 5 (x에다가 5를 더한뒤 대입해라)
		System.out.println("3. x =" + x);
		x -= 5; // x = x - 5
		System.out.println("4. x =" + x);

	}
}


